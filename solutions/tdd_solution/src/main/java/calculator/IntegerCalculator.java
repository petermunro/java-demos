package calculator;

public class IntegerCalculator {
    public static long add(int i, int j) {
        return i+j;
    }
    public static long subtract(int i, int j) {
        return i-j;
    }
    public static long multiply(int i, int j) {
        return i*j;
    }
    public static long divide(int i, int j) {
        return i/j;
    }
}
